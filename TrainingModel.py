# 3. Define the inference function
# ==========================================
def compare_models(input_image):

    if input_image is None:
        return None, "Please upload an image."

    pil_img = Image.fromarray(input_image).convert("RGB")

    # --------------------------------------
    # YOLOv11 Detection
    # --------------------------------------
    yolo_result = yolo_deployed(pil_img)

    yolo_output_img = yolo_result[0].plot()

    # --------------------------------------
    # MobileNet Classification
    # --------------------------------------
    if os.path.exists(mobilenet_weight_path):

        input_tensor = mobilenet_transforms(
            pil_img
        ).unsqueeze(0).to(device)

        with torch.no_grad():

            outputs = mobilenet_deployed(input_tensor)

            probabilities = torch.nn.functional.softmax(
                outputs[0],
                dim=0
            )

        best_prob, best_class = torch.max(
            probabilities,
            0
        )

        mobilenet_result = (
            f"MobileNet Prediction\n\n"
            f"Predicted Class ID: {best_class.item()}\n"
            f"Confidence Score: {best_prob.item() * 100:.2f}%"
        )

    else:

        mobilenet_result = (
            "MobileNet weights were not loaded. "
            "No classification result is available."
        )

    return yolo_output_img, mobilenet_result

