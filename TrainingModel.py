if os.path.exists(mobilenet_weight_path):

    # Load the saved state dictionary
    state_dict = torch.load(
        mobilenet_weight_path,
        map_location=device
    )

    # Automatically detect the number of output classes
    saved_classes = state_dict["classifier.3.weight"].shape[0]

    print(
        f"Detected number of classes from the trained model: {saved_classes}"
    )

    # Replace the final classifier layer
    num_ftrs = mobilenet_deployed.classifier[3].in_features

    mobilenet_deployed.classifier[3] = nn.Linear(
        num_ftrs,
        saved_classes
    )

    # Load trained weights
    mobilenet_deployed.load_state_dict(state_dict)

    mobilenet_deployed = mobilenet_deployed.to(device)
    mobilenet_deployed.eval()

    print("MobileNet model loaded successfully!")

