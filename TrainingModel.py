"""运行 MobileNet 训练（无缝衔接）Run MobileNet training (seamless transition)"""

import os
import glob
import torch
import torch.nn as nn
import torch.optim as optim
from torchvision import transforms, models
from torch.utils.data import Dataset, DataLoader
from PIL import Image

# 1. Define a custom dataset class for loading images and YOLO labels
class TextileDataset(Dataset):
    def __init__(self, img_dir, transform=None):
        self.img_dir = img_dir
        self.transform = transform

        # Search for all image files in the directory
        self.img_paths = []
        for ext in ['*.jpg', '*.jpeg', '*.png', '*.JPG', '*.JPEG', '*.PNG']:
            self.img_paths.extend(glob.glob(os.path.join(img_dir, ext)))

        # Read class labels from corresponding YOLO annotation files
        self.labels = []
        for img_path in self.img_paths:
            # Convert image path to label path
            # Example:
            # images/train/img001.jpg -> labels/train/img001.txt
            label_path = img_path.replace('images', 'labels')
            label_path = os.path.splitext(label_path)[0] + '.txt'

            if os.path.exists(label_path):
                with open(label_path, 'r') as f:
                    first_line = f.readline().strip()
                    if first_line:
                        class_id = int(first_line.split()[0])
                        self.labels.append(class_id)
                    else:
                        self.labels.append(0)
            else:
                # Assign class 0 if no label file exists
                self.labels.append(0)

    def __len__(self):
        return len(self.img_paths)

    def __getitem__(self, idx):
        img_path = self.img_paths[idx]
        image = Image.open(img_path).convert('RGB')
        label = self.labels[idx]

        if self.transform:
            image = self.transform(image)

        return image, label


# 2. Define data preprocessing and augmentation
data_transforms = transforms.Compose([
    transforms.Resize((224, 224)),
    transforms.RandomHorizontalFlip(),
    transforms.ToTensor(),
    transforms.Normalize(
        [0.485, 0.456, 0.406],
        [0.229, 0.224, 0.225]
    )
])

# 3. Load training and validation datasets
train_dir = '/content/dataset/split_data/images/train'
val_dir = '/content/dataset/split_data/images/val'

train_dataset = TextileDataset(train_dir, transform=data_transforms)
val_dataset = TextileDataset(val_dir, transform=data_transforms)

train_loader = DataLoader(
    train_dataset,
    batch_size=32,
    shuffle=True
)

val_loader = DataLoader(
    val_dataset,
    batch_size=32,
    shuffle=False
)

# Automatically determine the number of classes
all_labels = train_dataset.labels + val_dataset.labels
num_classes = len(set(all_labels)) if len(all_labels) > 0 else 1

print(
    f"MobileNet dataset loaded successfully!\n"
    f"Training samples: {len(train_dataset)}\n"
    f"Validation samples: {len(val_dataset)}\n"
    f"Detected classes: {num_classes}"
)

# 4. Build the MobileNetV3 model
mobilenet_model = models.mobilenet_v3_large(pretrained=True)

num_ftrs = mobilenet_model.classifier[3].in_features
mobilenet_model.classifier[3] = nn.Linear(
    num_ftrs,
    num_classes
)

device = torch.device(
    "cuda" if torch.cuda.is_available() else "cpu"
)

mobilenet_model = mobilenet_model.to(device)

criterion = nn.CrossEntropyLoss()
optimizer = optim.Adam(
    mobilenet_model.parameters(),
    lr=0.001
)

# 5. Train the model
print("Starting MobileNet training...")

num_epochs = 15

for epoch in range(num_epochs):

    mobilenet_model.train()
    running_loss = 0.0

    for inputs, labels in train_loader:

        inputs = inputs.to(device)
        labels = labels.to(device)

        optimizer.zero_grad()

        outputs = mobilenet_model(inputs)
        loss = criterion(outputs, labels)

        loss.backward()
        optimizer.step()

        running_loss += loss.item() * inputs.size(0)

    epoch_loss = (
        running_loss / len(train_dataset)
        if len(train_dataset) > 0
        else 0
    )

    print(
        f"Epoch {epoch + 1}/{num_epochs} "
        f"- Training Loss: {epoch_loss:.4f}"
    )

# 6. Save trained weights
torch.save(
    mobilenet_model.state_dict(),
    "mobilenet_textiles.pth"
)

print("Training completed successfully!")
print("Model weights saved as: mobilenet_textiles.pth")

